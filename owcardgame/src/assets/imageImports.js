// Simple per-key debounce for audio playback to avoid accidental double-plays
const __audioLastPlayedAt = {};
export function playAudioByKey(audioKey, options = {}) {
    const { debounceMs = 200 } = options;
    try {
        const now = Date.now();
        const last = __audioLastPlayedAt[audioKey] || 0;
        if (now - last < debounceMs) return;
        __audioLastPlayedAt[audioKey] = now;
        const src = getAudioFile(audioKey);
        if (!src) return;
        const a = new Audio(src);
        a.play().catch(() => {});
    } catch {}
}

// Function to get audio file by audio file name
export const getAudioFile = (audioFileName) => {
    // First try the static ability audio files
    if (abilityAudioFiles[audioFileName]) {
        return abilityAudioFiles[audioFileName];
    }
    
    // Then try the general audio files (like placement, overwatch-theme)
    if (audioFiles[audioFileName]) {
        return audioFiles[audioFileName];
    }
    
    // Fallback to dynamic require for intro sounds and other files
    try {
        return require(`./audio/${audioFileName}.mp3`).default;
    } catch (error) {
        console.log(`No audio file available: ${audioFileName}`);
        return null;
    }
};

// Alternative function that returns a promise for better error handling
export const getAudioFileAsync = async (audioFileName) => {
    try {
        const audioFile = require(`./audio/${audioFileName}.mp3`).default;
        return audioFile;
    } catch (error) {
        console.log(`No audio file available: ${audioFileName}`);
        return null;
    }
};
